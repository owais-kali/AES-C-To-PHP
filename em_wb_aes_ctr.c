	DWORD s0, s1, s2, s3, t0, t1, t2, t3;

	/* First table */
	for (i = 0; i < 16; i++) {
		j = plaintext[i];
		Temp1[i] = EncTable[i][j] ^ (BYTE)(((2 * i + 8) % 16) << 4) ^ (BYTE)((8 * i + 2) % 16);
	}

	/* 5R-AES */
	s0 = GETDWORD(Temp1) ^ rk[0];
	s1 = GETDWORD(Temp1 + 4) ^ rk[1];
	s2 = GETDWORD(Temp1 + 8) ^ rk[2];
